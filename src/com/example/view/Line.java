package com.example.view;

public class Line {

	private int start;

	private int end;

	private float lineCount;

	private float wordSpaceOffset;

	public int getStart() {
		return start;
	}

	public void setStart(int start) {
		this.start = start;
	}

	public int getEnd() {
		return end;
	}

	public void setEnd(int end) {
		this.end = end;
	}

	public float getLineCount() {
		return lineCount;
	}

	public void setLineCount(float lineCount) {
		this.lineCount = lineCount;
	}

	public float getWordSpaceOffset() {
		return wordSpaceOffset;
	}

	public void setWordSpaceOffset(float wordSpaceOffset) {
		this.wordSpaceOffset = wordSpaceOffset;
	}

}
